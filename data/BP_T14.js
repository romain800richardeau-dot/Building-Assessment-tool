                    <p>Les fichiers g&eacute;n&eacute;r&eacute;s &laquo; standards &raquo; sous-estiment syst&eacute;matiquement la chaleur estivale observ&eacute;e ces derni&egrave;res ann&eacute;es. L&rsquo;&eacute;cart est d&rsquo;autant plus marqu&eacute; que la p&eacute;riode de r&eacute;f&eacute;rence du fichier est ancienne.</p>
