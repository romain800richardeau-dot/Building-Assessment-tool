            <!-- ─── Synthèse ─── -->
            <div class="ms-retiens bp-section">
                <div class="ms-retiens-label bp-section-title">Je retiens</div>
                <h4>Les points essentiels du chapitre</h4>
                <ul>
                    <li>Le confort d&rsquo;&eacute;t&eacute; est un <strong>enjeu sanitaire et r&eacute;glementaire</strong> majeur, trait&eacute; par la RE2020 via les <strong>DH</strong> (degr&eacute;s-heures d&rsquo;inconfort).</li>
                    <li>Le confort thermique d&eacute;pend de <strong>6 param&egrave;tres</strong>, pas seulement de la temp&eacute;rature de l&rsquo;air. L&rsquo;indicateur pertinent est la <strong>temp&eacute;rature op&eacute;rative</strong>.</li>
                    <li>Les <strong>apports solaires par les vitrages</strong> repr&eacute;sentent 50 &agrave; 70 % de la chaleur estivale : la premi&egrave;re action est de <strong>prot&eacute;ger les baies par l&rsquo;ext&eacute;rieur</strong>.</li>
                    <li>L&rsquo;<strong>isolation est n&eacute;cessaire mais insuffisante</strong> : sans protection solaire, ventilation nocturne et inertie, un b&acirc;timent isol&eacute; devient un pi&egrave;ge thermique.</li>
                    <li>Hi&eacute;rarchie des leviers passifs : <strong>protection solaire &gt; toiture d&eacute;phasante &gt; ventilation nocturne &gt; inertie &gt; brasseurs d&rsquo;air</strong>.</li>
                </ul>
            </div>

