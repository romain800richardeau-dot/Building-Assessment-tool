                    <p>Le facteur solaire total g<sub>tot</sub> tient compte de la performance du textile <strong>et</strong> du vitrage associ&eacute;. La norme d&eacute;finit quatre vitrages de r&eacute;f&eacute;rence (A&nbsp;: simple clair, B&nbsp;: double isolant, C&nbsp;: double faible &eacute;missivit&eacute;, D&nbsp;: double &agrave; contr&ocirc;le solaire).</p>
