                    <p>Le facteur T<sub>e</sub> caract&eacute;rise la capacit&eacute; du textile &agrave; prot&eacute;ger de l&rsquo;ensoleillement direct. Plus T<sub>e</sub> est bas, moins l&rsquo;&eacute;nergie traverse la toile.</p>
