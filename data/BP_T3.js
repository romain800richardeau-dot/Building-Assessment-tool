                                    <p>Inspection visuelle tous les <strong>5 &agrave; 10 ans</strong> : d&eacute;chirures, perforations, &eacute;crasements, d&eacute;t&eacute;riorations des joints, accumulation de graisse. Un nettoyage int&eacute;rieur par h&eacute;risson rotatif est recommand&eacute; tous les <strong>10 ans</strong> environ.</p>
